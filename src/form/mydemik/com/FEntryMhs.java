    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        SessionFactory sf=HibernateUtil.getSessionFactory();
        Session s=sf.openSession();
        Prodi j=(Prodi) cbbProdi.getSelectedItem();
     
        Mahasiswa mhs = new Mahasiswa();
        Prodi prd=new Prodi();
       
        mhs.setNim(Integer.parseInt(txtNIM.getText()));
        mhs.setNama(txtNama.getText());
        mhs.setAlamat(txtAlamat.getText());
        mhs.setTlp(txtHP.getText());
        mhs.setProdi(prd);
        prd.setIdProdi(j.getIdProdi());
        Transaction tx=s.beginTransaction();
        s.saveOrUpdate(mhs);
        tx.commit();
        s.flush();
        s.close();
        try {
            fillTable(jTMhs);
        } catch (SQLException ex) {
            Logger.getLogger(FEntryMhs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

