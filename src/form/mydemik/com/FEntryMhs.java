    private void fillTable(final JTable table) throws SQLException 
    {
        try
        {
            DftTblModel_mahasiswa = new DefaultTableModel();
            DftTblModel_mahasiswa.addColumn("NIM");
            DftTblModel_mahasiswa.addColumn("NAMA");
            DftTblModel_mahasiswa.addColumn("PROGRAM STUDI");
            table.setModel(DftTblModel_mahasiswa);
            preparedStatement=connection.prepareStatement("SELECT * FROM mahasiswa");
            ResultSet res=preparedStatement.executeQuery();
            while(res.next())
            {
                DftTblModel_mahasiswa.addRow(new Object[]{
                res.getInt("nim"),
                res.getString("nama"),
                res.getString("idProdi")
                });
            }
                

        }
        catch (ClassCastException e) 
        {
                System.out.println(e);
        }
        
    }
    private void loadprodi() throws SQLException {
    cbbProdi.removeAllItems();
        List<Prodi> prdi=SuratService.getAllProdi();
        for(Prodi prdh:prdi){
            cbbProdi.addItem(prdh);
        }
    }
